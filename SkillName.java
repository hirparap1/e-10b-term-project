/**
 * The SkillName enum contains all skills returned by the API.
 * The Overall skill is technically not a skill but is returned as one by the
 * API.
 * The enum is ordered to match the order the skills are returned from the API.
 */
public enum SkillName {
    OVERALL, ATTACK, DEFENCE, STRENGTH,
    HITPOINTS, RANGED, PRAYER, MAGIC,
    COOKING, WOODCUTTING, FLETCHING,
    FISHING, FIREMAKING, CRAFTING, SMITHING,
    MINING, HERBLORE, AGILITY, THEIVING, SLAYER,
    FARMING, RUNECRAFT, HUNTER, CONSTRUCTION, SAILING
}